namespace Espamatica.Core
{
  public enum MediaTimeStringFormat
  {
    /// <summary>
    /// HH:mm:ss.ff.
    /// </summary>
    HHmmssff,

    /// <summary>
    /// HH:mm:ss.mmm.
    /// </summary>
    HHmmssmmm,

    /// <summary>
    /// HH:mm:ss.
    /// </summary>
    HHmmss,

    /// <summary>
    /// HH:mm.
    /// </summary>
    HHmm,

    /// <summary>
    /// mm:ss.ff.
    /// </summary>
    mmssff,

    /// <summary>
    /// mm:ss.mmm.
    /// </summary>
    mmssmmm,

    /// <summary>
    /// mm:ss.
    /// </summary>
    mmss,

    /// <summary>
    /// ss.ff.
    /// </summary>
    ssff,

    /// <summary>
    /// ss.mmm.
    /// </summary>
    ssmmm
  }
}
