namespace Espamatica.WpfToolkit
{
    public enum ComparisonOperator
    {
        /// <summary>
        /// Igual.
        /// </summary>
        Equal,

        /// <summary>
        /// Distinto.
        /// </summary>
        NotEqual,

        /// <summary>
        /// Mayor que.
        /// </summary>
        GreaterThan,

        /// <summary>
        /// Menor que.
        /// </summary>
        LessThan,

        /// <summary>
        /// Mayor o igual que.
        /// </summary>
        EqualOrGraterThan,

        /// <summary>
        /// Menor o igual que.
        /// </summary>
        EqualOrLessThan
    } // ComparisonOperatorEnum
}
